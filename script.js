// ─── APPLY LANGUAGE ────────────────────────────────────────────────────────────
function applyLang(lang) {
  const t = i18n[lang];
  if (!t) return;

  // Direction
  document.documentElement.lang = lang;
  document.body.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // All translated elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Save preference
  try { localStorage.setItem('yt-lang', lang); } catch(e) {}
}

// ─── BURGER MENU ───────────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  const burger = document.getElementById('burger');
  const navLinks = document.getElementById('nav-links');

  if (burger && navLinks) {
    burger.addEventListener('click', () => navLinks.classList.toggle('active'));
    navLinks.querySelectorAll('a').forEach(a => a.addEventListener('click', () => navLinks.classList.remove('active')));
    document.addEventListener('click', e => {
      if (!navLinks.contains(e.target) && !burger.contains(e.target)) navLinks.classList.remove('active');
    });
  }

  // Navbar shadow on scroll
  const navbar = document.getElementById('navbar');
  if (navbar) {
    window.addEventListener('scroll', () => {
      navbar.style.boxShadow = window.scrollY > 20 ? '0 10px 30px rgba(0,0,0,0.08)' : 'none';
    });
  }

  // Lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });

  // Restore saved lang
  let saved = 'it';
  try { saved = localStorage.getItem('yt-lang') || 'it'; } catch(e) {}
  applyLang(saved);

  // Contact form
  const form = document.getElementById('contact-form');
  const notice = document.getElementById('form-notice');
  if (form && notice) {
    form.addEventListener('submit', e => {
      e.preventDefault();
      const lang = document.documentElement.lang || 'it';
      const t = i18n[lang] || i18n.it;

      const nom = document.getElementById('nom')?.value.trim();
      const email = document.getElementById('email')?.value.trim();
      const sujet = document.getElementById('sujet')?.value.trim();
      const message = document.getElementById('message')?.value.trim();

      if (!nom || !email || !sujet || !message) {
        notice.textContent = t.form_error_fields;
        notice.style.color = '#c62828';
        return;
      }
      if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
        notice.textContent = t.form_error_email;
        notice.style.color = '#c62828';
        return;
      }
      notice.textContent = t.form_ok;
      notice.style.color = '#1f8f5f';
      form.reset();
    });
  }
});
